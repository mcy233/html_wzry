export function getTeamById(id) {
    return TEAMS.find(t => t.id === id);
}

export function getTeamsByCity(city) {
    return TEAMS.filter(t => t.city === city);
}

export function getStartersByTeamId(teamId) {
    const team = getTeamById(teamId);
    if (!team) return [];
    const roles = ['对抗路', '打野', '中路', '发育路', '游走'];
    return roles.map(role => {
        const candidates = team.players.filter(p => p.role === role);
        return candidates.sort((a, b) => b.rating - a.rating)[0];
    });
}
